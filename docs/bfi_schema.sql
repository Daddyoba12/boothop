-- BootHop Flight Intelligence (BFI) — Sprint 1 Schema
-- Run this in Supabase SQL editor to create all BFI tables.

-- Airport groups (e.g. "London" = LHR + LGW + STN + LTN + LCY)
CREATE TABLE IF NOT EXISTS bfi_airport_groups (
  id          uuid    DEFAULT gen_random_uuid() PRIMARY KEY,
  name        text    NOT NULL,  -- e.g. "London", "Lagos"
  city        text    NOT NULL,
  country     text    NOT NULL,
  created_at  timestamptz DEFAULT now()
);

-- Airports
CREATE TABLE IF NOT EXISTS bfi_airports (
  code         text PRIMARY KEY,  -- IATA code
  name         text NOT NULL,
  city         text NOT NULL,
  country      text NOT NULL,
  country_code text NOT NULL,
  timezone     text NOT NULL DEFAULT 'UTC',
  group_id     uuid REFERENCES bfi_airport_groups(id),
  created_at   timestamptz DEFAULT now()
);

-- Airlines
CREATE TABLE IF NOT EXISTS bfi_airlines (
  code        text PRIMARY KEY,  -- IATA code
  name        text NOT NULL,
  logo_url    text,
  alliance    text,
  baggage_kg  integer,
  rating      numeric(3,2) DEFAULT 4.0,
  created_at  timestamptz DEFAULT now()
);

-- Routes to monitor
CREATE TABLE IF NOT EXISTS bfi_routes (
  id                    uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  origin                text NOT NULL REFERENCES bfi_airports(code),
  destination           text NOT NULL REFERENCES bfi_airports(code),
  enabled               boolean DEFAULT true,
  priority              integer DEFAULT 5,
  scan_frequency_hours  integer DEFAULT 6,
  notes                 text,
  tags                  text[] DEFAULT '{}',
  created_at            timestamptz DEFAULT now(),
  UNIQUE(origin, destination)
);

-- One record per scan session (all routes scanned together)
CREATE TABLE IF NOT EXISTS bfi_search_runs (
  id               uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  started_at       timestamptz NOT NULL DEFAULT now(),
  completed_at     timestamptz,
  routes_searched  integer DEFAULT 0,
  offers_found     integer DEFAULT 0,
  provider         text NOT NULL,
  status           text NOT NULL DEFAULT 'running',  -- running | completed | failed
  error            text,
  duration_ms      integer
);

-- Individual flight offers from each scan
CREATE TABLE IF NOT EXISTS bfi_flight_offers (
  id                uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  search_run_id     uuid REFERENCES bfi_search_runs(id),
  route_id          uuid REFERENCES bfi_routes(id),
  origin            text NOT NULL,
  destination       text NOT NULL,
  airline_code      text NOT NULL,
  airline_name      text NOT NULL,
  flight_number     text,
  departure_at      timestamptz NOT NULL,
  arrival_at        timestamptz NOT NULL,
  travel_time_mins  integer,
  stops             integer DEFAULT 0,
  price_gbp         numeric(10,2) NOT NULL,
  currency          text DEFAULT 'GBP',
  cabin             text DEFAULT 'ECONOMY',
  baggage_included  boolean DEFAULT false,
  cabin_bag_incl    boolean DEFAULT true,
  fare_class        text,
  refundable        boolean DEFAULT false,
  change_fee_gbp    numeric(10,2),
  available_seats   integer,
  booking_url       text,
  provider          text NOT NULL,
  raw_data          jsonb,
  scanned_at        timestamptz DEFAULT now()
);

-- Daily price summary per route
CREATE TABLE IF NOT EXISTS bfi_daily_summaries (
  id                      uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  route_id                uuid REFERENCES bfi_routes(id),
  date                    date NOT NULL,
  cheapest_price_gbp      numeric(10,2),
  average_price_gbp       numeric(10,2),
  highest_price_gbp       numeric(10,2),
  cheapest_airline_code   text,
  cheapest_airline_name   text,
  offers_count            integer DEFAULT 0,
  saving_vs_yesterday_gbp numeric(10,2),
  created_at              timestamptz DEFAULT now(),
  UNIQUE(route_id, date)
);

-- Rolling statistics per route (upserted after each scan)
CREATE TABLE IF NOT EXISTS bfi_route_stats (
  route_id                 uuid PRIMARY KEY REFERENCES bfi_routes(id),
  all_time_lowest_gbp      numeric(10,2),
  all_time_highest_gbp     numeric(10,2),
  thirty_day_avg_gbp       numeric(10,2),
  ninety_day_avg_gbp       numeric(10,2),
  trend                    text DEFAULT 'stable',  -- rising | falling | stable
  trend_pct                numeric(5,2),
  cheapest_airline_code    text,
  cheapest_airline_name    text,
  opportunity_score        integer DEFAULT 50,
  recommendation           text DEFAULT 'HOLD',  -- BUY | WAIT | HOLD
  best_booking_window_days integer,
  best_month               text,
  last_updated             timestamptz DEFAULT now()
);

-- Alerts generated by the intelligence engine
CREATE TABLE IF NOT EXISTS bfi_alerts (
  id                   uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  route_id             uuid REFERENCES bfi_routes(id),
  type                 text NOT NULL,  -- price_drop | lowest_ever | price_rise | daily_summary
  title                text NOT NULL,
  message              text NOT NULL,
  price_gbp            numeric(10,2),
  previous_price_gbp   numeric(10,2),
  airline_code         text,
  airline_name         text,
  severity             text DEFAULT 'info',  -- info | success | warning | critical
  read                 boolean DEFAULT false,
  created_at           timestamptz DEFAULT now()
);

-- ── Indexes ────────────────────────────────────────────────────────────────────

CREATE INDEX IF NOT EXISTS bfi_flight_offers_route_id_idx   ON bfi_flight_offers(route_id);
CREATE INDEX IF NOT EXISTS bfi_flight_offers_scanned_at_idx ON bfi_flight_offers(scanned_at DESC);
CREATE INDEX IF NOT EXISTS bfi_daily_summaries_date_idx     ON bfi_daily_summaries(date DESC);
CREATE INDEX IF NOT EXISTS bfi_alerts_created_at_idx        ON bfi_alerts(created_at DESC);
CREATE INDEX IF NOT EXISTS bfi_alerts_read_idx              ON bfi_alerts(read) WHERE NOT read;

